 !   tet(1) = 0.;  rho(1) = 2.7;  hs(1) = 10000;  isym(1)= 5

    w = 15d0;  f = w/(2*pi);  z=-1d0; key = 1

! al(1)=alf*cos(gm);     al(2)=alf*sin(gm);
! α1=1  α2=2

    alf1 = 1d0;  alf2 = 2d0; 
    alf = sqrt(alf1**2 + alf2**2);  gm = atan2(alf2,alf1) 
    
    al(1)=alf*cos(gm);        al(2)=alf*sin(gm);
    
    write(2,2)f,w,z,al(1),al(2),alf,gm
    2  format('  f,w,z      = ',3f12.5/ &
                      '  al1,al2  = ',2(2f12.5,2x)/ &
                      '  alf,gm  = ',2f12.5,2x,f12.5)

    call MultiK_An(alf,gm,z)
    
    write(2,1)((i,j,Kaz(i,j),j=1,3),i=1,3)
1   format(' K(',i1,',',i1,') = ',2e24.17)    
    
