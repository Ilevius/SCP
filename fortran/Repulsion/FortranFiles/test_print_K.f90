    subroutine test_print_K
	use Mult_Glob; implicit none

    integer i,j
    
    real(8) gm,z,alf1,alf2 
    
    complex(8) alf
      
 !   C_6(1,1,1)=10.71427;    C_6(1,2,1)=7.14285;   C_6(1,3,1)=7.14285
 !   C_6(2,2,1)=10.71427;    C_6(2,3,1)=7.14285;   C_6(3,3,1)=10.71427
